import { useState } from 'react';
import { findLongestWord } from './Ecmascript';  // Import the function
import reactLogo from './assets/react.svg';
import viteLogo from '/vite.svg';
import './App.css';

function App() {
  const [count, setCount] = useState(0);
  const [longestWord, setLongestWord] = useState('');  // State for the longest word

  // Example words array
  const wordsArray = ["chat", "éléphant", "lion", "hippopotame"];

  // Call the function to find the longest word
  const handleFindLongestWord = () => {
    const result = findLongestWord(wordsArray);
    setLongestWord(result);  // Update the state with the result
  };

  return (
    <>
      <div>
        <a href="https://vite.dev" target="_blank">
          <img src={viteLogo} className="logo" alt="Vite logo" />
        </a>
        <a href="https://react.dev" target="_blank">
          <img src={reactLogo} className="logo react" alt="React logo" />
        </a>
      </div>
      <h1>Vite + React</h1>
      <div className="card">
        <button onClick={() => setCount((count) => count + 1)}>
          count is {count}
        </button>
        <p>
          Edit <code>src/App.jsx</code> and save to test HMR
        </p>
      </div>
      <p className="read-the-docs">
        Click on the Vite and React logos to learn more
      </p>

      {/* Button to trigger finding the longest word */}
      <button onClick={handleFindLongestWord}>
        Find Longest Word
      </button>
      {/* Display the result */}
      {longestWord && <p>{longestWord}</p>}
    </>
  );
}

export default App;

